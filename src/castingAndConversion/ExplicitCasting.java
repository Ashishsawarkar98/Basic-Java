package castingAndConversion;

public class ExplicitCasting {
	
	//converting high data type into low data type or Forceful casting.

	public static void main(String[] args) {
		
		long a = 5289;//8 byte
		int b =(int)a;//4 byte
		short c =(short)b;//2byte
		byte d =(byte)c; //1 byte
		
		//In explicit wait  may be occur data loss
		
		System.out.println(d);
		
		
		
		
		
		
		
		
		
		
		
	}

}
