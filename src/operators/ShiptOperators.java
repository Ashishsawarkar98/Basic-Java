package operators;

public class ShiptOperators {

	public static void main(String[] args) {
		int a=40;
		int b=2;
		System.out.println(a<<b); //less than less than = multiplication
		System.out.println(a>>b);
		 
		int c=60;
		int d=2;
		System.out.println(c<<d);
		System.out.println(c>>d);// grater than greater than= division
		
		int e=80;
		int f=2;
		System.out.println(e<<f);
		System.out.println(e>>f);
		
		int g=60;
		int h=3;
		System.out.println(g<<h);
		System.out.println(g>>h);
		

	}

}
