package logicalStarPatterns2;

public class AstarPattern {

	public static void main(String[] args) {
	
		//1*
		//2**
		//3***
		//4****
		//5*****
		
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
